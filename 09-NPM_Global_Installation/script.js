// Global Installation with NPM

/**
 * What is Global Installation?
 * - When you install a package globally, it can be used from any project or terminal window.
 * - Useful for CLI tools (e.g., nodemon, http-server, npm-check).
 *
 * Command to install globally:
 * npm install -g <package-name>
 *
 * Example:
 * npm install -g nodemon
 *
 * Now you can use 'nodemon' directly in your terminal from any directory.
 */

// Try this command in terminal after installing nodemon globally:
console.log("Now run: nodemon app.js"); // This will keep restarting app.js on file changes
